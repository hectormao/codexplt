package com.codex.codexplt.vo;

public class FormularioHijo {
	private Formulario hijo;
	private int orden;
	private String tipo;
	
	
	public Formulario getHijo() {
		return hijo;
	}
	public void setHijo(Formulario hijo) {
		this.hijo = hijo;
	}
	public int getOrden() {
		return orden;
	}
	public void setOrden(int orden) {
		this.orden = orden;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	
	
}
